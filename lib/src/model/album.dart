class Album {
  final int id;
  final String name;
  final String type;
  final DateTime releaseDate;
  final String catalogId;
  final String format;
  final String coverURL;
  final DateTime addedOn;

  Album({
    this.id,
    this.name,
    this.type,
    this.releaseDate,
    this.catalogId,
    this.format,
    this.coverURL,
    this.addedOn,
  });

}